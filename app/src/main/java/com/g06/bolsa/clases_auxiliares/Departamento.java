package com.g06.bolsa.clases_auxiliares;

public class Departamento {
    private String id;
    private String nombre;


    public Departamento() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
