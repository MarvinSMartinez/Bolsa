package com.g06.bolsa.clases_auxiliares;

public class PerfilCandidato {
    String idperfilcandidato;
    String iddepartamento;
    String idusuario;
    String nombre;
    String apellido;
    String dui;
    String nit;
    public PerfilCandidato( ) {

    }

    public String getIdperfilcandidato() {
        return idperfilcandidato;
    }

    public void setIdperfilcandidato(String idperfilcandidato) {
        this.idperfilcandidato = idperfilcandidato;
    }

    public String getIddepartamento() {
        return iddepartamento;
    }

    public void setIddepartamento(String iddepartamento) {
        this.iddepartamento = iddepartamento;
    }

    public String getIdusuario() {
        return idusuario;
    }

    public void setIdusuario(String idusuario) {
        this.idusuario = idusuario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getDui() {
        return dui;
    }

    public void setDui(String dui) {
        this.dui = dui;
    }

    public String getNit() {
        return nit;
    }

    public void setNit(String nit) {
        this.nit = nit;
    }
}
